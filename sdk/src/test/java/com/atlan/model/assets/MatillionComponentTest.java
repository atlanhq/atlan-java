    @Test
    void serdeCycleMatillionComponent() throws IOException {
        assertNotNull(full, "Unable to build sample instance of MatillionComponent,");
        final int hash = full.hashCode();
        // Builder equivalency
        assertEquals(
                full.toBuilder().build(),
                full,
                "Unable to converting MatillionComponent via builder back to its original state,");
        // Serialization
        final String serialized = full.toJson(MockAtlanTenant.client);
        assertNotNull(serialized, "Unable to serialize sample instance of MatillionComponent,");
        assertEquals(full.hashCode(), hash, "Serialization mutated the original value,");
        // Deserialization
        final MatillionComponent frodo = MockAtlanTenant.client.readValue(serialized, MatillionComponent.class);
        assertNotNull(frodo, "Unable to reverse-read serialized value back into an instance of MatillionComponent,");
        // Serialized equivalency
        String backAgain = frodo.toJson(MockAtlanTenant.client);
        assertEquals(backAgain, serialized, "Serialization is not equivalent after serde loop,");
        // Deserialized equivalency
        assertEquals(frodo, full, "Deserialization is not equivalent after serde loop,");
    }
